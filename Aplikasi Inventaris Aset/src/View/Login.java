    private void btnloginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnloginActionPerformed
        try {
            java.sql.Statement stat = conn.createStatement();
            ResultSet result = stat.executeQuery("select * from tbl_user where username='" + textuser.getText() +"'");
            if (result.next()){
                if (textpass.getText().equals(result.getString("password"))){
                    u_nama = result.getString("nama");
                    u_tingkat = result.getString("tingkat");
                    Login.setU_nama(u_nama);
                    Login.setU_tingkat(u_tingkat);
                    if(u_nama.equals("admin")){
                        Menu menu = new Menu();
                        menu.setVisible(true);
                        menu.hakAksesAdmin(); 
                    }else{
                        Menu menu = new Menu();
                        menu.setVisible(true);
                        menu.hakAksesUser(); 
                    }
                } else {
                     JOptionPane.showMessageDialog(null, "Password Anda Salah");
                     textpass.setText(null);
                     textpass.setText(null);
                     textpass.requestFocus();
                }
            } else{
                 JOptionPane.showMessageDialog(null, "Username Tidak ditemukan ");
                 textuser.setText(null);
                 textuser.requestFocus();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Gagal"+ e);
        }
    }//GEN-LAST:event_btnloginActionPerformed

