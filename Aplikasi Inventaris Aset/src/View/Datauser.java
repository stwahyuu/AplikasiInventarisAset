    private void btnsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpanActionPerformed
        if (textnama.getText().equals("")){
        JOptionPane.showConfirmDialog(null, "Kolom Nama Silahkan Diisi");
        } else if (textuser.getText().equals("")){
            JOptionPane.showConfirmDialog(null, "Kolom Username Silahkan Diisi");
        } else if (textpass.getText().equals("")){
            JOptionPane.showConfirmDialog(null, "Kolom Password Silahkan Diisi");
        } else {
            String sql = "insert into tbl_user (id_user,nama,username,password,tingkat) values (?,?,?,?,?)";
            try {
                PreparedStatement stat = conn.prepareStatement(sql);
                JOptionPane.showMessageDialog(null,"Data Berhasil Disimpan");
                stat.setString(1, textid.getText());
                stat.setString(2, textnama.getText());
                stat.setString(3, textuser.getText());
                stat.setString(4, textpass.getText());
                stat.setString(5, (String) cmbtingkat.getSelectedItem());
                stat.executeUpdate();
                dataTable();
                textnama.requestFocus();
            } catch(SQLException e){
                JOptionPane.showMessageDialog(null, "Penyimpanan Data Gagal"+ e);
            }
        }
    }//GEN-LAST:event_btnsimpanActionPerformed

