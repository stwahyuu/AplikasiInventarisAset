    private void btnubahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnubahActionPerformed
        String sql = "Update tbl_user set id_user=?, nama=?, username=?, password=?, tingkat=? where id_user='"+ textid.getText() +"'";
        try {
            PreparedStatement stat = conn.prepareStatement(sql);
                stat.setString(1, textid.getText());
                stat.setString(2, textnama.getText());
                stat.setString(3, textuser.getText());
                stat.setString(4, textpass.getText());
                stat.setString(5, (String) cmbtingkat.getSelectedItem());
                stat.executeUpdate();
                JOptionPane.showMessageDialog(null, "Data Berhasil Diubah ");
                dataTable();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Data Gagal Diubah"+ e);
        }
                
    }//GEN-LAST:event_btnubahActionPerformed

