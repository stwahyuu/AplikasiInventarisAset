    public void dataTable(){
        Object[] baris = {"No","ID","Nama","Username","Password","Tingkat"};
        tm = new DefaultTableModel(null, baris);
        tbluser.setModel(tm);
        String sql = "select * from tbl_user order by id_user asc";
        try {
            java.sql.Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while (hasil.next()){
                String id_user = hasil.getString("Id_user");
                String nama = hasil.getString("Nama");
                String username = hasil.getString("Username");
                String password = hasil.getString("Password");
                String tingkat = hasil.getString("Tingkat");
                String [] data = {"",id_user,nama,username,password,tingkat};
                tm.addRow(data);
                noTable();
                lebarKolom();
                }
            }catch (SQLException e){
                JOptionPane.showMessageDialog(null, "Data Tidak Bisa ditampikan");
        }
        
    }
    
    
