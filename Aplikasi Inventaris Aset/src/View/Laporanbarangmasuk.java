    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{
            File file = new File("src/Laporan/laporanbarangmasuk.jrxml");
            String cb1 = (String) cmbbarang1.getSelectedItem();
            String cb2 = (String) cmbbarang2.getSelectedItem();
            jasdes = JRXmlLoader.load(file);
            parmet.put("barang1", cb1);
            parmet.put("barang2", cb2);
            jasrep = JasperCompileManager.compileReport(jasdes);
            jasprint = JasperFillManager.fillReport(jasrep, parmet, conn);
            JasperViewer jasview = new JasperViewer(jasprint, false);
            jasview.setExtendedState(jasview.getExtendedState() | javax.swing.JFrame.MAXIMIZED_BOTH);
            jasview.setVisible(true);
            jasview.toFront();
        }catch(JRException e){
            JOptionPane.showMessageDialog(null, "Gagal Membuka Laporan"+ e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

