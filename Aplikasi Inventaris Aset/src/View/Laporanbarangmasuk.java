    public void tampilcmbbox1(){
        cmbbarang1.removeAllItems();
        cmbbarang1.addItem("---Pilih ID Barang---");
        try {
            String sql = "select * from tbl_brgmasuk order by id_brgmasuk asc";
            java.sql.Statement stat = conn.createStatement();
            ResultSet res = stat.executeQuery(sql);
            while(res.next()){
                cmbbarang1.addItem((res.getString("id_brgmasuk")));
            }
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }
    
